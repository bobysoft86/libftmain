#include "libft.h"
#include <stdio.h>

int	ft_toupper(int c)
{
	if (c >= 97 && c <= 122)
		c = c - 32;
	return (c);
	return (0);
}

int main (void)
{
int a;

a = 'o';

printf("%d\n", ft_toupper(a));


}
