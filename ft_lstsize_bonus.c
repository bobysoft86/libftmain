#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*tmp;

	i = 0;
	tmp = (t_list *)lst;
	while (tmp)
	{
		i++;
		tmp = tmp ->next;
	}
	return (i);
}
